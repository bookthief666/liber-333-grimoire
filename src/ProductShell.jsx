function normalizeText(value = '') {
  return value
    .replace(/[^\p{L}\p{N}]+/gu, ' ')
    .trim()
    .toUpperCase();
}

function readStoredPreferences() {
  try {
    const parsed = JSON.parse(localStorage.getItem(PREFERENCES_KEY) || 'null');
    return { ...DEFAULT_PREFERENCES, ...(parsed || {}) };
  } catch {
    return DEFAULT_PREFERENCES;
  }
}

function hasSeenOrientation() {
  try {
    return localStorage.getItem(ORIENTATION_KEY) === 'true';
  } catch {
    return false;
  }
}

function findButton(label) {
  const target = normalizeText(label);
  return Array.from(document.querySelectorAll('button')).find((button) => {
    const text = normalizeText(button.textContent || '');
    return text === target || (target === 'GRIMOIRE' && text.startsWith('GRIMOIRE'));
  });
}

function navigateLegacyApp(mode, { beginOracle = false } = {}) {
  window.dispatchEvent(new CustomEvent('liber333:navigate', { detail: { mode, beginOracle } }));

  window.setTimeout(() => {
    findButton(MODE_LABELS[mode])?.click();

    if (mode === 'oracle' && beginOracle) {
      window.setTimeout(() => findButton('BEGIN CONSULTATION')?.click(), 90);
    }
  }, 80);
}

function Modal({ title, description, onClose, children, className = '' }) {
  const dialogRef = useRef(null);
  const titleId = `liber-dialog-${normalizeText(title).toLowerCase().replace(/\s+/g, '-')}`;

  useEffect(() => {
    const previous = document.activeElement;
    const dialog = dialogRef.current;
    const focusable = dialog?.querySelector(
      'button:not([disabled]), [href], input:not([disabled]), select:not([disabled]), textarea:not([disabled]), [tabindex]:not([tabindex="-1"])',
    );
    focusable?.focus();

    const onKeyDown = (event) => {
      if (event.key === 'Escape') {
        event.preventDefault();
        onClose();
        return;
      }

      if (event.key !== 'Tab' || !dialog) return;
      const items = Array.from(
        dialog.querySelectorAll(
          'button:not([disabled]), [href], input:not([disabled]), select:not([disabled]), textarea:not([disabled]), [tabindex]:not([tabindex="-1"])',
        ),
      );
      if (!items.length) return;
      const first = items[0];
      const last = items[items.length - 1];
      if (event.shiftKey && document.activeElement === first) {
        event.preventDefault();
        last.focus();
      } else if (!event.shiftKey && document.activeElement === last) {
        event.preventDefault();
        first.focus();
      }
    };

    document.addEventListener('keydown', onKeyDown);
    return () => {
      document.removeEventListener('keydown', onKeyDown);
      previous?.focus?.();
    };
  }, [onClose]);

  return (
    <div className="liber-shell-backdrop" role="presentation">
      <button className="liber-shell-dismiss-layer" aria-label="Close dialog" onClick={onClose} />
      <section
        ref={dialogRef}
        role="dialog"
        aria-modal="true"
        aria-labelledby={titleId}
        aria-describedby={description ? `${titleId}-description` : undefined}
        className={`liber-shell-dialog ${className}`}
      >
        <header className="liber-shell-dialog-header">
          <div>
            <p className="liber-shell-kicker">LIBER CCCXXXIII</p>
            <h2 id={titleId}>{title}</h2>
            {description ? <p id={`${titleId}-description`}>{description}</p> : null}
          </div>
          <button className="liber-shell-close" onClick={onClose} aria-label="Close">
            ×
          </button>
        </header>
        {children}
      </section>
    </div>
  );
}

