export default function ProductShell({ children }) {
  const [orientationOpen, setOrientationOpen] = useState(() => !hasSeenOrientation());
  const [activePanel, setActivePanel] = useState(null);
  const [activeMode, setActiveMode] = useState('oracle');
  const [preferences, setPreferences] = useState(readStoredPreferences);
  const [installPrompt, setInstallPrompt] = useState(null);
  const [installed, setInstalled] = useState(() => window.matchMedia?.('(display-mode: standalone)').matches || false);

  const currentModeLabel = useMemo(() => MODE_LABELS[activeMode] || MODE_LABELS.oracle, [activeMode]);

  useEffect(() => {
    document.documentElement.dataset.liberEffects = preferences.effects;
    document.documentElement.dataset.liberTextScale = preferences.textScale;
    document.documentElement.dataset.liberMotion = preferences.motion;
    document.documentElement.classList.toggle('liber-force-reduced-motion', preferences.motion === 'reduced');
    document.documentElement.classList.toggle('liber-force-full-motion', preferences.motion === 'full');
    try {
      localStorage.setItem(PREFERENCES_KEY, JSON.stringify(preferences));
    } catch {
      // Presentation preferences are non-critical.
    }
  }, [preferences]);

  useEffect(() => {
    const trackMode = (event) => {
      const button = event.target.closest?.('button');
      if (!button) return;
      const text = normalizeText(button.textContent || '');
      const matched = Object.entries(MODE_LABELS).find(([, label]) => {
        const normalizedLabel = normalizeText(label);
        return text === normalizedLabel || (label === 'GRIMOIRE' && text.startsWith(normalizedLabel));
      });
      if (matched) setActiveMode(matched[0]);
    };
    document.addEventListener('click', trackMode, true);
    return () => document.removeEventListener('click', trackMode, true);
  }, []);

  useEffect(() => {
    const beforeInstall = (event) => {
      event.preventDefault();
      setInstallPrompt(event);
    };
    const appInstalled = () => {
      setInstalled(true);
      setInstallPrompt(null);
    };
    window.addEventListener('beforeinstallprompt', beforeInstall);
    window.addEventListener('appinstalled', appInstalled);
    return () => {
      window.removeEventListener('beforeinstallprompt', beforeInstall);
      window.removeEventListener('appinstalled', appInstalled);
    };
  }, []);

  const markOrientationSeen = () => {
    try {
      localStorage.setItem(ORIENTATION_KEY, 'true');
    } catch {
      // Orientation can still close when storage is unavailable.
    }
    setOrientationOpen(false);
  };

  const choosePath = (path) => {
    markOrientationSeen();
    setActiveMode(path.mode);
    navigateLegacyApp(path.mode, { beginOracle: path.id === 'ask' });
  };

  const navigate = (mode) => {
    setActivePanel(null);
    setActiveMode(mode);
    navigateLegacyApp(mode, { beginOracle: false });
  };

  const install = async () => {
    if (!installPrompt) return;
    await installPrompt.prompt();
    await installPrompt.userChoice.catch(() => null);
    setInstallPrompt(null);
  };

  return (
    <>
      {children}

      {!orientationOpen ? (
        <div className="liber-utility-rail" aria-label="Grimoire guidance and settings">
          <button
            onClick={() => setActivePanel('help')}
            aria-label={`Explain ${currentModeLabel.toLowerCase()} mode`}
            title={`Explain ${currentModeLabel.toLowerCase()} mode`}
          >
            <span aria-hidden="true">?</span>
            <small>{currentModeLabel}</small>
          </button>
          <button onClick={() => setActivePanel('settings')} aria-label="Open reading environment settings" title="Reading environment">
            <span aria-hidden="true">⚙</span>
            <small>SETTINGS</small>
          </button>
          {!installed && installPrompt ? (
            <button onClick={install} aria-label="Install Liber 333" title="Install Liber 333">
              <span aria-hidden="true">⇩</span>
              <small>INSTALL</small>
            </button>
          ) : null}
        </div>
      ) : null}

      {orientationOpen ? <Orientation onChoose={choosePath} onDismiss={markOrientationSeen} /> : null}
      {activePanel === 'help' ? <HelpPanel mode={activeMode} onClose={() => setActivePanel(null)} onNavigate={navigate} /> : null}
      {activePanel === 'settings' ? (
        <SettingsPanel
          preferences={preferences}
          onChange={setPreferences}
          onResetOrientation={() => {
            try {
              localStorage.removeItem(ORIENTATION_KEY);
            } catch {
              // Non-critical.
            }
            setActivePanel(null);
            setOrientationOpen(true);
          }}
          onClose={() => setActivePanel(null)}
        />
      ) : null}
    </>
  );
}
